import java.lang.*;

class DeclarationAndInitialization
{
    public static void main(String[] args)
    {
        // int a;
        // System.out.println(a); // this will give an error as variable "a" is only declared and not initialized

        int b = 5;
        System.out.println(b); // 5
        b = 12;
        System.out.println(b); // 12
    }
}
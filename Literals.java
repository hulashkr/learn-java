import java.lang.*;

class Literals
{
    public static void main(String[] args)
    {
        byte a = 10;
        byte b = 0b1010;
        byte c = 012;
        byte d = 0xA;

        System.out.println(a); // 10
        System.out.println(b); // 10
        System.out.println(c); // 10
        System.out.println(d); // 10
    }
}